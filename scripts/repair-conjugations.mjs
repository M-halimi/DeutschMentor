// Repair script: detect missing conjugations, regenerate only missing ones, validate all 342 verbs
// Run: npx tsx scripts/repair-conjugations.mjs <token> <projectRef>

import { generateConjugationsByRule } from '../src/verbs/conjugation/rules.ts'

const TOKEN = process.argv[2]
const PROJECT_REF = process.argv[3] || 'solksyxdlmjtthhuhacj'

const API = `https://api.supabase.com/v1/projects/${PROJECT_REF}/database/query`

const REQUIRED_TENSES = ['praesens', 'praeteritum', 'perfekt', 'plusquamperfekt', 'futur_i', 'futur_ii', 'konjunktiv_i', 'konjunktiv_ii', 'imperativ', 'passiv']
const PERSONS = ['ich', 'du', 'er_sie_es', 'wir', 'ihr', 'Sie']

async function sql(query) {
  const res = await fetch(API, {
    method: 'POST',
    headers: { 'Authorization': `Bearer ${TOKEN}`, 'Content-Type': 'application/json' },
    body: JSON.stringify({ query }),
  })
  const text = await res.text()
  if (!res.ok) {
    console.error(`SQL ERROR (${res.status}):`, text)
    process.exit(1)
  }
  return JSON.parse(text)
}

function tenseInsertSQL(verbId, tense, forms) {
  const cols = ['verb_id', 'tense', 'ich', 'du', 'er_sie_es', 'wir', 'ihr', 'Sie', 'version']
  const vals = [
    `'${verbId}'`,
    `'${tense}'`,
    ...PERSONS.map(p => {
      const v = (forms[p] || '').replace(/'/g, "''")
      return `'${v}'`
    }),
    '1'
  ]
  return `INSERT INTO verb_conjugations (${cols.join(', ')}) VALUES (${vals.join(', ')});`
}

async function main() {
  console.log('=== CONJUGATION REPAIR SCRIPT ===\n')

  // Step 1: Get all verbs
  console.log('Step 1: Fetching all verbs...')
  const verbs = await sql(`SELECT id, infinitive, auxiliary, partizip_2, verb_type, cefr_level FROM german_verbs ORDER BY infinitive`)
  console.log(`  Found ${verbs.length} verbs`)

  // Step 2: Get all existing conjugations
  console.log('\nStep 2: Fetching existing conjugations...')
  const existingRows = await sql(`SELECT verb_id, tense, COUNT(*) as cnt FROM verb_conjugations GROUP BY verb_id, tense ORDER BY verb_id, tense`)
  const existingMap = {}
  for (const row of existingRows) {
    if (!existingMap[row.verb_id]) existingMap[row.verb_id] = new Set()
    existingMap[row.verb_id].add(row.tense)
  }

  // Step 3: Audit
  console.log('\nStep 3: Auditing all verbs...')
  const missingCounts = {}
  const verbIssues = []
  let totalMissing = 0

  for (const v of verbs) {
    const existing = existingMap[v.id] || new Set()
    const missing = REQUIRED_TENSES.filter(t => !existing.has(t))
    
    // Check for required fields
    const missingFields = []
    if (!v.auxiliary) missingFields.push('auxiliary')
    if (!v.partizip_2) missingFields.push('partizip_2')
    if (!v.verb_type) missingFields.push('verb_type')
    if (!v.cefr_level) missingFields.push('cefr_level')

    if (missing.length > 0 || missingFields.length > 0) {
      totalMissing += missing.length
      for (const t of missing) {
        missingCounts[t] = (missingCounts[t] || 0) + 1
      }
      verbIssues.push({
        id: v.id,
        infinitive: v.infinitive,
        auxiliary: v.auxiliary,
        existingTenses: [...existing],
        missing,
        missingFields,
      })
    }
  }

  console.log(`  Verbs with issues: ${verbIssues.length}/${verbs.length}`)
  console.log(`  Total missing tenses: ${totalMissing}`)
  console.log('\n  Missing tense breakdown:')
  for (const [tense, count] of Object.entries(missingCounts).sort((a, b) => b[1] - a[1])) {
    console.log(`    ${tense}: ${count}`)
  }

  const verbsWithFieldIssues = verbIssues.filter(v => v.missingFields.length > 0)
  if (verbsWithFieldIssues.length > 0) {
    console.log(`\n  Verbs with missing fields:`)
    for (const v of verbsWithFieldIssues) {
      console.log(`    ${v.infinitive}: missing ${v.missingFields.join(', ')}`)
    }
  }

  // Step 4: Generate and insert missing conjugations
  console.log('\nStep 4: Generating missing conjugations...')
  let inserted = 0
  let generated = 0
  let errors = 0

  const BATCH_SIZE = 50

  for (let i = 0; i < verbIssues.length; i += BATCH_SIZE) {
    const batch = verbIssues.slice(i, i + BATCH_SIZE)
    const insertStatements = []
    
    for (const v of batch) {
      if (v.missing.length === 0) continue
      
      try {
        const allTenses = generateConjugationsByRule(v.infinitive, v.auxiliary || 'haben')
        generated++
        
        for (const tense of v.missing) {
          const forms = allTenses[tense]
          if (!forms) {
            console.warn(`  WARNING: ${v.infinitive} - ${tense} not generated by rules engine`)
            continue
          }
          insertStatements.push(tenseInsertSQL(v.id, tense, forms))
          inserted++
        }
      } catch (err) {
        errors++
        console.error(`  ERROR generating for ${v.infinitive}:`, err.message)
      }
    }

    if (insertStatements.length > 0) {
      const sqlBatch = insertStatements.join('\n')
      console.log(`  Batch ${Math.floor(i / BATCH_SIZE) + 1}: inserting ${insertStatements.length} tenses...`)
      await sql(sqlBatch)
    }

    // Progress
    const pct = Math.min(100, Math.round((i + batch.length) / verbIssues.length * 100))
    if (pct % 25 === 0 || i + BATCH_SIZE >= verbIssues.length) {
      console.log(`  Progress: ${i + batch.length}/${verbIssues.length} verbs (${pct}%)`)
    }
  }

  console.log(`\n  Results: ${inserted} tenses inserted, ${generated} verbs generated, ${errors} errors`)

  // Step 5: Validate
  console.log('\nStep 5: Validating all 342 verbs...')
  const validation = await sql(`
    SELECT v.infinitive, COUNT(c.id) as conj_count,
      COUNT(*) FILTER (WHERE c.tense = 'praesens') as has_praesens,
      COUNT(*) FILTER (WHERE c.tense = 'praeteritum') as has_praeteritum,
      COUNT(*) FILTER (WHERE c.tense = 'perfekt') as has_perfekt,
      COUNT(*) FILTER (WHERE c.tense = 'plusquamperfekt') as has_plusquamperfekt,
      COUNT(*) FILTER (WHERE c.tense = 'futur_i') as has_futur_i,
      COUNT(*) FILTER (WHERE c.tense = 'futur_ii') as has_futur_ii,
      COUNT(*) FILTER (WHERE c.tense = 'konjunktiv_i') as has_konjunktiv_i,
      COUNT(*) FILTER (WHERE c.tense = 'konjunktiv_ii') as has_konjunktiv_ii,
      COUNT(*) FILTER (WHERE c.tense = 'imperativ') as has_imperativ,
      COUNT(*) FILTER (WHERE c.tense = 'passiv') as has_passiv
    FROM german_verbs v
    LEFT JOIN verb_conjugations c ON c.verb_id = v.id
    GROUP BY v.id, v.infinitive
    ORDER BY conj_count, v.infinitive
  `)

  const fullCount = validation.filter(v => v.conj_count === 10).length
  const partialCount = validation.filter(v => v.conj_count > 0 && v.conj_count < 10).length
  const emptyCount = validation.filter(v => v.conj_count === 0).length
  const missingTenses = validation.filter(v => v.conj_count < 10).map(v => ({
    infinitive: v.infinitive,
    count: v.conj_count,
    missing: REQUIRED_TENSES.filter(t => v[`has_${t}`] === 0)
  }))

  console.log(`  Full (10/10): ${fullCount}`)
  console.log(`  Partial (<10): ${partialCount}`)
  console.log(`  Empty (0): ${emptyCount}`)
  
  if (missingTenses.length > 0) {
    console.log(`\n  Verbs still missing tenses:`)
    for (const v of missingTenses) {
      console.log(`    ${v.infinitive}: ${v.count}/10 - missing ${v.missing.join(', ')}`)
    }
  }

  console.log('\n=== REPAIR COMPLETE ===')
}

main().catch(err => {
  console.error('FATAL:', err)
  process.exit(1)
})
